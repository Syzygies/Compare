// Check result against known answers
public func checkResult(_ n: Int, _ result: [Int64]) {
    // Print result
    print(result.map(String.init).joined(separator: " "))
    
    // Validate
    if n <= 12 {
        if let expected = answers(n) {
            if expected == result {
                print("✓")
            } else {
                print("✗")
                print(expected.map(String.init).joined(separator: " "))
            }
        }
    } else {
        print("?")
    }
}